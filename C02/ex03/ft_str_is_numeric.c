int		ft_str_is_numeric(char *str)
{
	int i;
	int n;

	i = 0;
	n = 1;
	while (str[i])
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
		{
			n = 0;
			break ;
		}
		else if (str[i] == '\0')
		{
			n = 1;
		}
		else
			n = 1;
		i++;
	}
	return (n);
}
