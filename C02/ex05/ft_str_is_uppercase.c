int		ft_str_is_uppercase(char *str)
{
	int i;
	int n;

	i = 0;
	n = 1;
	while (str[i])
	{
		if (!(str[i] >= 'A' && str[i] <= 'Z'))
		{
			n = 0;
			break ;
		}
		else
			n = 1;
		i++;
	}
	return (n);
}
