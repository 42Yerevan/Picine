int		ft_str_is_alpha(char *str)
{
	int i;
	int n;

	i = 0;
	n = 1;
	while (str[i])
	{
		if (!((str[i] >= 'A' && str[i] <= 'Z') ||
					(str[i] >= 'a' && str[i] <= 'z')))
		{
			n = 0;
			break ;
		}
		else if (str[i] == '\0')
		{
			n = 1;
		}
		else
			n = 1;
		i++;
	}
	return (n);
}
