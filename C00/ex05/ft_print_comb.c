#include <unistd.h>

void	ft_print_comb(void)
{
	int a;
	int b;
	int c;

	a = 48 - 1;
	while (a++ < 55)
	{
		b = a;
		while (b++ < 56)
		{
			c = b;
			while (c++ < 57)
			{
				write(1, &a, 1);
				write(1, &b, 1);
				write(1, &c, 1);
				if (!(a == 55 && b == 56 && c == 57))
				{
					write(1, ",", 1);
					write(1, " ", 1);
				}
			}
		}
	}
}
